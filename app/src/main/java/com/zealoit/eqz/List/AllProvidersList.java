package com.zealoit.eqz.List;

public class AllProvidersList {

    private int status;
    private String queueCount;
    private String serviceName;
    private int providerId;
    private String providerTitle;
    private int srviceid;
    private int businessId;

    public int getStatus() {
        return status;
    }

    public void setStatus(int status){
        this.status = status;
    }

    public String getQueueCount() {
        return queueCount;
    }

    public void setQueueCount(String queueCount){
        this.queueCount = queueCount;
    }

    public String getServiceName() {
        return serviceName;
    }

    public void setServiceName(String serviceName){
        this.serviceName = serviceName;
    }

    public int getProviderId() {
        return providerId;
    }

    public void setProviderId(int providerId){
        this.providerId = providerId;
    }

    public String getProviderTitle() {
        return providerTitle;
    }

    public void setProviderTitle(String providerTitle){
        this.providerTitle = providerTitle;
    }

    public int getSrviceid() {
        return srviceid;
    }

    public void setSrviceid(int srviceid){
        this.srviceid = srviceid;
    }

    public int getBusinessId() {
        return businessId;
    }

    public void setBusinessId(int businessId){
        this.businessId = businessId;
    }
}
